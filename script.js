document.getElementById('ram8-input').addEventListener('click', function () {
    let ram = 0;
    let ramInput = document.getElementById('ram-cost');
    ramOutput = parseInt(ramInput.innerText);
    ramInput.innerText = ram;

    //best-Price
    let bestPriceInput = document.getElementById('best-price');
    let bestPriceOutput = parseInt(bestPriceInput.innerText);
    // ram 
    let ramCostInput = document.getElementById('ram-cost');
    let ramCostOutput = parseInt(ramCostInput.innerText);
    // storage 
    let storageCostInput = document.getElementById('storage-cost');
    let storageCostOutput = parseInt(storageCostInput.innerText);
    // delivery 
    let deliveryCostInput = document.getElementById('delivery-cost');
    let deliveryCostOutput = parseInt(deliveryCostInput.innerText);

    // Total-Sum 
    let totalSumInput = document.getElementById('sub-total');
    let totalSumOutput = parseInt(totalSumInput.innerText);
    totalSum = bestPriceOutput + ramCostOutput + storageCostOutput + deliveryCostOutput;
    totalSumInput.innerText = totalSum;
    //total
    let totalInput = document.getElementById('total');
    let totalOutput = parseInt(totalInput.innerText);
    totalInput.innerText = 0;
});
